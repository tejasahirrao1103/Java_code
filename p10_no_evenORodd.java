import java.util.Scanner;
public class p10_no_evenORodd {
    public static void main (String arg[])
    {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter number:- ");
        int n= sc.nextInt();
        if (n%2==0)
        {
            System.out.println("Number is odd");
        }
        else
        {
            System.out.println("Number is odd");
        }

    }
    
}
