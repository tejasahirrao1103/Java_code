public class p3_iterals {
    public static void main(String arg[])
    {
        // Numric litrals
        int a = 075;
        System.out.println("this is ocatal literal 075 :- "+a); //octal

        int b= 0b001;
        System.out.println("this is binary literal 0b001 :-"+b);//binary

        int c=0x1a;
        System.out.println("this is hexa literals 0xla :- "+c);//hexa

        long num1 = 10_00_00_000L;
        System.out.println("this is special way to write number by saprating with underscore :- "+num1);//special way

        // Char litrals
        char ch = 'X';
        System.out.println("this is our normal character literal X :- "+ch);//char

        char uni_ch='\u0021';
        System.out.println("this is unicode character literal \\u0021 :- "+uni_ch);//unicode char

        // String litrals
        String str="hello everyone";
        System.out.println("this is String normal litrral :- "+str); // string
        

    }
}
