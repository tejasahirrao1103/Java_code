public class p2_datatype {
    public static void main(String arg[])
    {   
        byte a = 5;
        System.out.println("This is byete "+ a);

        short c = 10;
        System.out.println("This is short "+ c);

        int b = 11;
        System.out.println("This is int "+ b);

        long d = 123243l;
        System.out.println("This is int "+d);

        float e = 13233.323f;
        System.out.println("This is float "+e);

        double f = 3842348.343949;
        System.out.println("This is double "+f);

        char g = 'X';
        System.out.println("this is char "+g);

        boolean h = true;
        System.out.println("This is boolean "+ h);
        
    
    }
    
}
